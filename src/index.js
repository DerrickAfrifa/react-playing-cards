const selectDeck = deckType => {
  if (deckType === "big-face") {return cardsTwoColorBF}
  if (deckType === "big-face four-color") {return cardsFourColorBF}
  if (deckType === "four-color") {return cardsFourColor}

  return cardsTwoColor
}

function Card(props) {
  const {card, front = true, back, deckType, height} = props
  const style = {height}
  const cards = selectDeck(deckType)

  if (back) {
    return (
      <img src={b} className="card card__back" alt="card-back" style={style} />
    )
  }

  if (front) {
    return (
      <img src={cards[card]} className="card" alt={card} style={style} />
    )
  }
}

export default Card
